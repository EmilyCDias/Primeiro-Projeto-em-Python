#Um algoritmo em programação é uma sequência finita de passos bem definidos e ordenados que descrevem como resolver um problema ou realizar uma tarefa. Esses passos podem ser implementados em um programa de computador usando uma linguagem de programação.

#Características de um algoritmo:
#Finitude: O algoritmo deve ter um número limitado de etapas e terminar após ser executado.
#Clareza: Cada passo deve ser claro e sem ambiguidade.
#Entrada: Pode haver zero ou mais valores de entrada necessários para o algoritmo funcionar.
#Saída: Deve produzir pelo menos uma saída ou resultado.
#Eficácia: Todas as instruções devem ser práticas e executáveis dentro de um tempo finito.
#Exemplo de um algoritmo simples:
#Problema: Criar um algoritmo que some dois números.

#Passos do algoritmo:

#Leia o número 1.
#Leia o número 2.
#Some os dois números.
#Exiba o resultado.
#Implementação em Python:

# Entrada
numero1 = int(input("Digite o primeiro número: "))
numero2 = int(input("Digite o segundo número: "))

# Processamento
soma = numero1 + numero2

# Saída
print("A soma é:", soma)
#Os algoritmos são fundamentais na programação porque formam a base para a criação de software eficiente e funcional. Eles podem ser simples, como no exemplo acima, ou complexos, como algoritmos de aprendizado de máquina ou criptografia.

# Formas comuns de representação de algoritmos:
# 1. Descrição Narrativa: Usar linguagem natural para descrever os passos.
# 2. Fluxograma: Representação gráfica com símbolos padrão (ex: retângulos, losangos, setas).
# 3. Pseudocódigo: Linguagem intermediária que descreve a lógica, próxima à programação.
# 4. Diagrama de Blocos: Versão simplificada do fluxograma, usando formas básicas.
# 5. Tabelas de Decisão: Lista de condições e ações em formato tabular.
# 6. Linguagem de Programação: O algoritmo representado diretamente no código.
